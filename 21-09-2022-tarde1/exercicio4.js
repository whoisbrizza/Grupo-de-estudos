// Exercício 4: Dias da Semana

function diaDaSemana (dia){
    if(dia === 1){
        console.log("Dia da semana é domingo.")
    }else if(dia === 2){
        console.log("Dia da semana é segunda.")
    }else if(dia === 3){
        console.log("Dia da semana é terça-feira.")
    }else if(dia === 4){
        console.log("Dia da semana é quarta-feira.")
    }else if(dia === 5){
        console.log("Dia da semana é quinta-feira.")
    }else if(dia === 6){
        console.log("Dia da semana é sexta-feira.")
    }else if(dia === 7){
        console.log("Dia da semana é sábado.")
    }else{
        console.log("Dia da semana é inválido.")
    }
}

diaDaSemana(1)
diaDaSemana(2)
diaDaSemana(8)